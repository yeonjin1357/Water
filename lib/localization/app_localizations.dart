  static String _currentLanguage = 'ko';

  static void setLanguage(String languageCode) {
    if (_localizedValues.containsKey(languageCode)) {
      _currentLanguage = languageCode;
    }
  }

  static String get currentLanguage => _currentLanguage;

  static String get(String key, [String? param]) {
    String value = _localizedValues[_currentLanguage]?[key] ?? key;
    if (param != null) {
      value = value.replaceAll('%s', param);
    }
    return value;
  }

  static String getDrinkName(String drinkType) {
    switch (drinkType.toLowerCase()) {
      case 'water':
        return get('water');
      case 'tea':
        return get('tea');
      case 'coffee':
        return get('coffee');
      case 'juice':
        return get('juice');
      case 'milk':
        return get('milk');
      default:
        return drinkType;
    }
  }

  static String getMonthName(int month) {
    if (_currentLanguage == 'ko') {
      return '${month}월';
    } else {
      const months = [
        'Jan',
        'Feb',
        'Mar',
        'Apr',
        'May',
        'Jun',
        'Jul',
        'Aug',
        'Sep',
        'Oct',
        'Nov',
        'Dec',
      ];
      return months[month - 1];
    }
  }

  static String getWeekdayName(int weekday) {
    const weekdayKeys = [
      'monday',
      'tuesday',
      'wednesday',
      'thursday',
      'friday',
      'saturday',
      'sunday',
    ];
    return get(weekdayKeys[weekday - 1]);
  }
